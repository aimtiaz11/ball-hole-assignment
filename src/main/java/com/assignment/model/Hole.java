package com.assignment.model;

/**
 * Model of the Hole
 */
public class Hole {

    /**
     * Arbitrary size attribute
     */
    private int size;

    public Hole(int size) {
        this.size = size;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }
}
